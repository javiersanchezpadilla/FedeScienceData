print('Asignamos un valor entero\n')
valor = 12
print(valor)
valor = valor + 10
print(valor)

print('Asignamos una cadena a la variable\n')
valor = 'cadena de texto'
print(valor)
valor = valor + " con otra cadena de texto"
print(valor)

print('Asignamos valores booleanos a la variable\n')
valor = True
print(valor)
valor = False
print(valor)

print('Asignamos valores reales\n')
valor = 3.1415926
print(valor)
valor = valor ** 3
print(valor)

print('Asignamos una lista')
valor = [1, 2, 3, 4]
